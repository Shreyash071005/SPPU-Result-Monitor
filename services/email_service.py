import smtplib

from email.mime.text import (
    MIMEText,
)

from email.mime.multipart import (
    MIMEMultipart,
)

from datetime import datetime

import pytz

from config import (
    EMAIL_ADDRESS,
    EMAIL_APP_PASSWORD,
    NOTIFICATION_EMAIL,
    RESULT_DASHBOARD_URL,
)


class EmailService:

    # ==========================================================
    # SEND EMAIL NOTIFICATION
    # ==========================================================

    def send_new_result_email(

            self,

            result,

    ):

        message = MIMEMultipart(
            "alternative"
        )

        message["Subject"] = (
            "NEW SPPU RESULT DECLARED"
        )

        message["From"] = (
            EMAIL_ADDRESS
        )

        message["To"] = (
            NOTIFICATION_EMAIL
        )

        html_body = self.generate_html_body(
            result
        )

        message.attach(

            MIMEText(

                html_body,

                "html",

            )

        )

        with smtplib.SMTP(

                "smtp.gmail.com",

                587,

        ) as server:

            server.starttls()

            server.login(

                EMAIL_ADDRESS,

                EMAIL_APP_PASSWORD,

            )

            server.sendmail(

                EMAIL_ADDRESS,

                NOTIFICATION_EMAIL,

                message.as_string(),

            )

    # ==========================================================
    # GENERATE EMAIL TEMPLATE
    # ==========================================================

    def generate_html_body(

            self,

            result,

    ):

        india_timezone = pytz.timezone(
            "Asia/Kolkata"
        )

        current_time = (

            datetime.now(
                india_timezone
            ).strftime(
                "%d %B %Y %I:%M:%S %p"
            )

        )

        return f"""

        <html>

        <body>

            <h2>
                NEW SPPU RESULT DECLARED
            </h2>

            <hr>

            <p>
                <b>Title :</b>
                <br>
                {result["title"]}
            </p>

            <p>
                <b>Academic Year :</b>
                <br>
                {result["year"]}
            </p>

            <p>
                <b>Credit Pattern :</b>
                <br>
                {result["pattern"]}
            </p>

            <p>
                <b>Result Declaration Date :</b>
                <br>
                {result["result_date"]}
            </p>

            <p>
                <b>Monitoring Time :</b>
                <br>
                {current_time}
            </p>

            <hr>

            <p>
                Official SPPU Result Dashboard:
            </p>

            <a href="{RESULT_DASHBOARD_URL}">
                Visit Official Result Dashboard
            </a>

            <hr>

            <p>
                This is an automated notification
                generated by the SPPU Result Monitor.
            </p>

        </body>

        </html>

        """